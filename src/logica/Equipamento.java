public class Equipamento {
    private String tag;
    private double tempodisponivel;
    private double tempoindisponivel;

    /**
     * @return the tag
     */
    public String getTag() {
        return tag;
    }

    /**
     * @param tag the tag to set
     */
    public void setTag(String tag) {
        this.tag = tag;
    }

    /**
     * @return the tempodisponivel
     */
    public double getTempodisponivel() {
        return tempodisponivel;
    }

    /**
     * @param tempodisponivel the tempodisponivel to set
     */
    public void setTempodisponivel(double tempodisponivel) {
        this.tempodisponivel = tempodisponivel;
    }

    /**
     * @return the tempoindisponivel
     */
    public double getTempoindisponivel() {
        return tempoindisponivel;
    }

    /**
     * @param tempoindisponivel the tempoindisponivel to set
     */
    public void setTempoindisponivel(double tempoindisponivel) {
        this.tempoindisponivel = tempoindisponivel;
    }


}

